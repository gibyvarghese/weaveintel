#!/usr/bin/env node
// Phase 0 (restructure) — dependency inventory generator.
// Reads every workspace package.json (packages/*, clients/*, apps/*) and emits a
// Markdown "before" snapshot: each package's internal deps, reverse-deps (who
// depends on it), published name/version/private flag, and summary counts.
// Deterministic (sorted) so re-runs produce stable diffs.
import { readFileSync, readdirSync, existsSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const GROUPS = ['packages', 'clients', 'apps'];

// Discover every workspace that has a package.json.
const workspaces = [];
for (const group of GROUPS) {
  const groupDir = join(ROOT, group);
  if (!existsSync(groupDir)) continue;
  for (const entry of readdirSync(groupDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const pkgPath = join(groupDir, entry.name, 'package.json');
    if (!existsSync(pkgPath)) continue;
    const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
    workspaces.push({
      group,
      dir: `${group}/${entry.name}`,
      name: pkg.name || `(unnamed:${group}/${entry.name})`,
      version: pkg.version || '0.0.0',
      private: pkg.private === true,
      deps: { ...(pkg.dependencies || {}) },
      devDeps: { ...(pkg.devDependencies || {}) },
      peerDeps: { ...(pkg.peerDependencies || {}) },
    });
  }
}

const nameSet = new Set(workspaces.map((w) => w.name));
const isInternal = (dep) => nameSet.has(dep);

// Build forward internal-dep map + reverse-dep map.
const byName = new Map(workspaces.map((w) => [w.name, w]));
const reverse = new Map(workspaces.map((w) => [w.name, new Set()]));
for (const w of workspaces) {
  w.internalDeps = new Set();
  for (const [scope, bag] of [['dep', w.deps], ['dev', w.devDeps], ['peer', w.peerDeps]]) {
    for (const dep of Object.keys(bag)) {
      if (isInternal(dep)) {
        w.internalDeps.add(dep);
        reverse.get(dep).add(`${w.name}${scope === 'dep' ? '' : `(${scope})`}`);
      }
    }
  }
}

const sortedWs = [...workspaces].sort((a, b) => a.name.localeCompare(b.name));
const published = sortedWs.filter((w) => !w.private);
const privateWs = sortedWs.filter((w) => w.private);

// ── Emit machine-readable JSON (for later phases to diff against) ──
const json = {
  generatedFrom: 'restructure/framework-app-separation Phase 0 baseline',
  counts: {
    totalWorkspaces: workspaces.length,
    packages: workspaces.filter((w) => w.group === 'packages').length,
    clients: workspaces.filter((w) => w.group === 'clients').length,
    apps: workspaces.filter((w) => w.group === 'apps').length,
    published: published.length,
    private: privateWs.length,
  },
  workspaces: sortedWs.map((w) => ({
    name: w.name, dir: w.dir, version: w.version, private: w.private,
    internalDeps: [...w.internalDeps].sort(),
    dependents: [...reverse.get(w.name)].sort(),
    externalDepCount: Object.keys(w.deps).filter((d) => !isInternal(d)).length,
  })),
};
writeFileSync(join(ROOT, 'scripts', 'dependency-inventory.json'), JSON.stringify(json, null, 2) + '\n');

// ── Emit Markdown fragment ──
const lines = [];
lines.push('## Before snapshot — dependency inventory');
lines.push('');
lines.push(`Generated by \`scripts/gen-dependency-inventory.mjs\` on branch \`restructure/framework-app-separation\` (baseline off \`collab-phase-0\`).`);
lines.push('');
lines.push('### Counts');
lines.push('');
lines.push('| Metric | Count |');
lines.push('|---|---|');
lines.push(`| Total workspaces | ${json.counts.totalWorkspaces} |`);
lines.push(`| \`packages/*\` | ${json.counts.packages} |`);
lines.push(`| \`clients/*\` | ${json.counts.clients} |`);
lines.push(`| \`apps/*\` | ${json.counts.apps} |`);
lines.push(`| Published (non-private) | ${json.counts.published} |`);
lines.push(`| Private (not published) | ${json.counts.private} |`);
lines.push('');
lines.push('### Published package names (the `@weaveintel/*` surface today)');
lines.push('');
lines.push('| Package | Version | Internal deps | Dependents (reverse-deps) |');
lines.push('|---|---|---|---|');
for (const w of published) {
  const deps = [...w.internalDeps].sort().join(', ') || '—';
  const revs = [...reverse.get(w.name)].sort().join(', ') || '**(no internal consumers)**';
  lines.push(`| \`${w.name}\` | ${w.version} | ${deps} | ${revs} |`);
}
lines.push('');
lines.push('### Private workspaces (apps + non-published clients)');
lines.push('');
lines.push('| Workspace | Dir | Internal deps (count) |');
lines.push('|---|---|---|');
for (const w of privateWs) {
  lines.push(`| \`${w.name}\` | ${w.dir} | ${w.internalDeps.size} |`);
}
lines.push('');
lines.push('### Leaf packages (zero internal consumers — merge/example candidates)');
lines.push('');
const leaves = published.filter((w) => reverse.get(w.name).size === 0);
lines.push(leaves.length
  ? leaves.map((w) => `- \`${w.name}\``).join('\n')
  : '_(none)_');
lines.push('');

writeFileSync(join(ROOT, 'scripts', 'dependency-inventory.md'), lines.join('\n') + '\n');
console.log(`workspaces=${workspaces.length} published=${published.length} private=${privateWs.length} leaves=${leaves.length}`);
console.log('wrote scripts/dependency-inventory.json and scripts/dependency-inventory.md');
